/******AFTER SELECTING STREAM AND COURSE TYPE*******/
declare @stream_id int
declare @course_type_id int

SELECT
	/*STREAM.STREAM_ID,
	STREAM.STREAM_NAME,
	COURSE_TYPE.COURSE_TYPE_ID,
	COURSE_TYPE.COURSE_TYPE_NAME,*/
	COURSES.COURSE_ID,
	COURSES.COURSE_NAME,
	LOGO_LOCATION
	FROM 
	STREAM STREAM JOIN COURSES COURSES
	ON STREAM.STREAM_ID = COURSES.STREAM_ID
		JOIN COURSE_TYPE COURSE_TYPE
		ON COURSES.COURSE_TYPE_ID = COURSE_TYPE.COURSE_TYPE_ID
WHERE @stream_id = STREAM.STREAM_ID and @course_type_id = COURSE_TYPE.COURSE_TYPE_ID
ORDER BY STREAM.STREAM_ID, COURSE_TYPE.COURSE_TYPE_ID, COURSES.COURSE_ID


















