/******AFTER SELECTING STREAM AND COURSE TYPE*******/
declare @stream_id int
declare @course_type_id int

SELECT
	/*STREAMSTREAM_ID,
	STREAMS.STREAM_NAME,
	COURSE_TYPE.COURSE_TYPE_ID,
	COURSE_TYPE.COURSE_TYPE_NAME,*/
	COURSES.COURSE_ID,
	COURSES.COURSE_NAME,
	LOGO_LOCATION
	FROM 
	STREAMS STREAMS JOIN COURSES COURSES
	ON STREAMS.STREAM_ID = COURSES.STREAM_ID
		JOIN COURSE_TYPE COURSE_TYPE
		ON COURSES.COURSE_TYPE_ID = COURSE_TYPE.COURSE_TYPE_ID
WHERE @stream_id = STREAMS.STREAM_ID and @course_type_id = COURSE_TYPE.COURSE_TYPE_ID
ORDER BY STREAMS.STREAM_ID, COURSE_TYPE.COURSE_TYPE_ID, COURSES.COURSE_ID


















