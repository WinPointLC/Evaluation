DECLARE @email_id VARCHAR(150)
DECLARE @password varchar(32)

SELECT	USER_PROFILE.USER_ID,
		FIRST_NAME, 
		LAST_NAME, 
		PHOTO_LOCATION, 
		USER_PROFILE.USER_CATEGORY_ID,
		EMPLOYEE_CATEGORY.EMPLOYEE_CATEGORY_ID
	FROM USER_PROFILE JOIN USER_CATEGORY 
		ON USER_PROFILE.USER_CATEGORY_ID = USER_CATEGORY.USER_CATEGORY_ID
			LEFT OUTER JOIN EMPLOYEE_DETAILS D
			ON USER_PROFILE.USER_ID = D.USER_ID
				LEFT OUTER JOIN EMPLOYEE_CATEGORY
				ON D.EMPLOYEE_CATEGORY_ID = EMPLOYEE_CATEGORY.EMPLOYEE_CATEGORY_ID
	WHERE USER_PROFILE.EMAIL_ID = @email_id AND USER_PROFILE.PASSWORD = @password 


