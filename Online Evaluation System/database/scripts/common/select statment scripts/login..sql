SELECT	USER_PROFILE.USERID,
		FIRST_NAME, 
		EMAIL, 
		PHOTO, 
		USER_PROFILE.USER_CATEGORY_ID, 
		USER_CATEGORY.USER_CATEGORY_NAME ,
		EMPLOYEE_CATEGORY.EMPLOYEE_CATEGORY_NAME
	FROM USER_PROFILE  
		JOIN USER_CATEGORY 
		ON USER_PROFILE.USER_CATEGORY_ID = USER_CATEGORY.USER_CATEGORY_ID
			LEFT OUTER JOIN EMPLOYEE_DETAILS D
			ON USER_PROFILE.USERID = D.USERID
				LEFT OUTER JOIN EMPLOYEE_CATEGORY
				ON D.EMPLOYEE_CATEGORY_ID = EMPLOYEE_CATEGORY.EMPLOYEE_CATEGORY_ID
	WHERE (USER_PROFILE.EMAIL = @email OR USER_PROFILE.MOBILE_NUMBER = @mobile_number) AND USER_PROFILE.PASSWORD = @password 


